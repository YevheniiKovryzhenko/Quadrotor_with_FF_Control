function animation_3D = create_n_spaced_objs(ids,p,q, ...
    scaling, fig)

n = length(ids);
animation_3D = cell(1,n);

for i = 1:n
    % animation_3D{i} = lib.visual3D.create_body_NED(fig, p(:,1), scaling);
    animation_3D{i} = lib.visual3D.create_quadcopter_NED(fig, p(:,1), scaling);
    animation_3D{i} = lib.visual3D.update(animation_3D{i},q(:,ids(i)),p(:,ids(i)));
end
end