function R_z = q2dcm_z(q)
% /**
%  * Corresponding body z-axis to an attitude quaternion /
%  * last orthogonal unit basis vector
%  */
    R_z = zeros(3,1);
    a = q(1);   %w
    b = q(2);   %x
    c = q(3);   %y
    d = q(4);   %z
    R_z(1) = 2 * (a * c + b * d);
    R_z(2) = 2 * (c * d - a * b);
    R_z(3) = a * a - b * b - c * c + d * d;
end